-- 📝 Markdown Blog Engine: Database Schema

-- THIS FILE IS INTENTIONALLY EMPTY.

-- Why?
-- This project is a "Flat-File CMS" (Content Management System).
-- The database is the computer's actual hard drive file system.
-- 
-- Benefits:
-- 1. No database to maintain, backup, or pay for.
-- 2. You can write blog posts using any text editor on your computer.
-- 3. Version control: You can commit your blog posts to Git!
-- 
-- Trade-offs:
-- 1. Very hard to do complex queries (e.g. "Find all posts by author X between Date Y and Z").
-- 2. Does not scale horizontally well (if you have 5 server instances, they all need the files).
